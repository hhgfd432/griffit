function toggleMobileMenu() {
    const menu = document.getElementById('mobileMenu');
    menu.classList.toggle('active');
}

// ====================
// SCROLL ANIMATIONS (AOS)
// ====================

function initScrollAnimations() {
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('aos-animate');
            }
        });
    }, observerOptions);
    
    document.querySelectorAll('[data-aos]').forEach(el => {
        observer.observe(el);
    });
}

// ====================
// SMOOTH SCROLL
// ====================

function initSmoothScroll() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
                document.getElementById('mobileMenu').classList.remove('active');
            }
        });
    });
}

// ====================
// NAVBAR SCROLL EFFECT
// ====================

function initNavbarScroll() {
    const navbar = document.querySelector('.navbar');
    
    window.addEventListener('scroll', () => {
        const currentScroll = window.pageYOffset;
        
        if (currentScroll > 100) {
            navbar.style.boxShadow = '0 4px 20px rgba(0,0,0,0.1)';
        } else {
            navbar.style.boxShadow = 'none';
        }
    });
}

// ====================
// EMAILJS FORM HANDLING
// ====================

function initEmailJS() {
    emailjs.init(EMAILJS_CONFIG.PUBLIC_KEY);
    
    const form = document.getElementById('contactForm');
    if (!form) return;
    
    form.addEventListener('submit', async (e) => {
        e.preventDefault();
        
        const submitBtn = form.querySelector('button[type="submit"]');
        const originalText = submitBtn.textContent;
        const currentLang = localStorage.getItem('griffit-lang') || 'en';
        
        submitBtn.textContent = translations[currentLang].form_sending;
        submitBtn.disabled = true;
        submitBtn.style.opacity = '0.7';
        
        const name = document.getElementById('name').value;
        const email = document.getElementById('email').value;
        const service = document.getElementById('service').value;
        const message = document.getElementById('message').value;
        
        const prices = {
            'online-store': '€60+',
            'business-website': '€60+',
            'design': '€40+',
            'support': '€20/month'
        };
        
        const price = prices[service] || '€60+';
        
        const serviceNames = {
            'online-store': 'Online Store',
            'business-website': 'Business Website',
            'design': 'UI/UX Design',
            'support': 'Monthly Support'
        };
        
        try {
            const result = await emailjs.send(
                EMAILJS_CONFIG.SERVICE_ID,
                EMAILJS_CONFIG.TEMPLATE_ID,
                {
                    from_name: name,
                    from_email: email,
                    service: serviceNames[service] || service,
                    price: price,
                    message: message
                }
            );
            
            console.log('Email sent:', result.text);
            showFormMessage('success', translations[currentLang].form_success);
            form.reset();
            
        } catch (error) {
            console.error('Email error:', error);
            showFormMessage('error', translations[currentLang].form_error);
        } finally {
            submitBtn.textContent = originalText;
            submitBtn.disabled = false;
            submitBtn.style.opacity = '1';
        }
    });
}

function showFormMessage(type, message) {
    const form = document.getElementById('contactForm');
    
    const oldMessage = form.querySelector('.form-message');
    if (oldMessage) oldMessage.remove();
    
    const messageDiv = document.createElement('div');
    messageDiv.className = `form-message ${type}`;
    messageDiv.textContent = message;
    
    form.insertBefore(messageDiv, form.firstChild);
    
    setTimeout(() => {
        messageDiv.style.opacity = '0';
        setTimeout(() => messageDiv.remove(), 300);
    }, 5000);
}

// ====================
// PARALLAX EFFECT
// ====================

function initParallax() {
    const floatingCards = document.querySelectorAll('.floating-card');
    if (!floatingCards.length) return;
    
    window.addEventListener('mousemove', (e) => {
        const x = (e.clientX / window.innerWidth - 0.5) * 20;
        const y = (e.clientY / window.innerHeight - 0.5) * 20;
        
        floatingCards.forEach((card, index) => {
            const speed = (index + 1) * 0.5;
            card.style.transform = `translate(${x * speed}px, ${y * speed}px)`;
        });
    });
}

